package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        //escape sequence \n - it inserts a new line
        System.out.println("Hello students. \nToday we have Java class");
        System.out.println("-------------------------------------------");

        //escape sequence \t - inserts horizontal tab (i.e. long space)
        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");
        System.out.println("-------------------------------------------");

        //escape sequence \\ - inserts one (1) backslash
        System.out.println("\\Mama");
        System.out.println("-------------------------------------------");

        //escape sequence \" - inserts a double quote (")
        System.out.println("My favorite TV show is \"The Walking Dead\".");


    }

}

/*
            What I learned today
    1. \n - it is for a new line
    2. \t - it is a paragraph space
    3. \\ - it inserts one (1) backslash
    4. \" - inserts a double quote
 */

